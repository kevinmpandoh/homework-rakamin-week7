export const hitungLuasPersegi = (sisi) => {
    return sisi * sisi;
}

export const hitungKelilingPersegi = (sisi) => {
    return 4 * sisi;
}

export const hitungLuasPersegiPanjang = (panjang, lebar) => {
    return panjang * lebar;
}

export const hitungKelilingPersegiPanjang = (panjang, lebar) => {
    return ( 2 * panjang) + (2 * lebar);
}
